export default function BottomNav() {
  const pathname = usePathname();
  const [hasSession, setHasSession] = useState(false);

  useEffect(() => {
    supabase.auth.getSession().then(({ data }) => {
      setHasSession(!!data.session);
    });

    const {
      data: { subscription },
    } = supabase.auth.onAuthStateChange((_event, session) => {
      setHasSession(!!session);
    });

    return () => subscription.unsubscribe();
  }, []);

  // Con sesión el ítem lleva al perfil; sin sesión, a iniciar sesión/registrarse
  const profileHref = hasSession ? "/profile" : "/auth/login";
  const profileActive =
    pathname.startsWith("/profile") || pathname.startsWith("/auth");

  return (
    <nav className="fixed bottom-0 left-0 right-0 z-50 bg-card-bg border-t border-border">
      <div className="max-w-lg mx-auto flex items-center justify-around py-2">
        <Link
          href="/"
          className={`flex flex-col items-center gap-1 px-4 py-2 transition-colors ${
            pathname === "/" ? "text-primary" : "text-foreground/60 hover:text-foreground"
          }`}
        >
          <HomeIcon active={pathname === "/"} />
          <span className="text-xs font-medium">Home</span>
        </Link>

        <Link
          href="/rank"
          className={`flex flex-col items-center gap-1 px-4 py-2 transition-colors ${
            pathname === "/rank" ? "text-primary" : "text-foreground/60 hover:text-foreground"
          }`}
        >
          <RankIcon active={pathname === "/rank"} />
          <span className="text-xs font-medium">Rank</span>
        </Link>

        <Link
          href="/post"
          className="flex items-center justify-center w-12 h-12 rounded-full bg-gradient-to-r from-primary to-accent text-white shadow-lg hover:scale-105 transition-transform"
        >
          <PlusIcon />
        </Link>

        <Link
          href="/dashboard"
          className={`flex flex-col items-center gap-1 px-4 py-2 transition-colors ${
            pathname === "/dashboard" ? "text-primary" : "text-foreground/60 hover:text-foreground"
          }`}
        >
          <DashboardIcon active={pathname === "/dashboard"} />
          <span className="text-xs font-medium">Dashboard</span>
        </Link>

        <Link
          href={profileHref}
          className={`flex flex-col items-center gap-1 px-4 py-2 transition-colors ${
            profileActive ? "text-primary" : "text-foreground/60 hover:text-foreground"
          }`}
        >
          <ProfileIcon active={profileActive} />
          <span className="text-xs font-medium">
            {hasSession ? "Perfil" : "Entrar"}
          </span>
        </Link>
      </div>
    </nav>
  );
}
